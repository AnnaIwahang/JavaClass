package methodoverridding;

public class MethodOverridding {
	/*
	 * ============== Method Overriding ==============
	 *  # use to achieve Polymorphism
	 *  # There must be Is-A relationship between two classes.
	 *  # if both parents and child class have same method(same name,return type,list of parameters) but different implementation then we can do method overriding.
	 *  
	 *  # inherit  -> use same as parent.
	 *  # override -> do modification/change in parent class's method in child class.
	 *  
	 *  syntax:
	 *    
	 *    class A(){
	 *        
	 *        void add(int a, int b){
	 *       //parent class logics
	 *       
	 *       }
	 *    
	 *    }
	 *    class B extends A{
	 *    
	 *    @Overriding
	 *    void add(int a, int b){
	 *    
	 *    }
	 *  
	 */

}
