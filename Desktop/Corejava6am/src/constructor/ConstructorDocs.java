package constructor;

public class ConstructorDocs {
	/*
	 * ======================================= Constructor ======================================
	 * # use to create object.
	 * # special type of method which has same name as class name which is known as constructor.
	 * 
	 * # if there is no any user defined constructor in class than java provides default constructor.
	 * 
	 * # does not have return type.
	 * 
	 * # Types:
	 *  1> Default Constructor :
	 *     # use to create object and set default values to it.
	 *     
	 *     syntax:
	 *     
	 *     class_name(){
	 *     
	 *           //set default values to object
	 *      }
	 *    
	 *  2> Parameterized Constructor :
	 *     # use to create object and set dynamic values to object
	 *     
	 *     syntax:
	 *     
	 *     class_name(arg1, arg2,........,argn){
	 *         
	 *         //set dynamic or user passed values to object.
	 *         
	 *     }
	 *  
	 *  
	 *
	 */

}
