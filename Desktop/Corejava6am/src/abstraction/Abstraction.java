package abstraction;

public class Abstraction {
	/*
	 * ===================== Abstraction ==========================
	 * # process of hiding implementation details in the program is known as abstraction.
	 * 
	 * # To achieve abstraction :
	 * 
	 *   a> Abstract class:
	 *      # class which contains abstract methods(unimplemented method) and non-abstract(implemented method) methods is known as abstract class.
	 *           
	 *           #Abstract method:
	 *            -> method which does not have implemented or body.
	 *            e.g. 
	 *               abstract void add(); //no body
	 *               abstract int getValue();
	 *      
	 *      # we can't create object of abstract class.
	 *      # every abstract class must have atleast one child class and child class must override abstract methods.
	 *      # level of abstraction 0 to 100%
	 *        
	 *         syntax:
	 *           
	 *           abstract class class_name{
	 *                   
	 *                   //abstract method
	 *                   
	 *                   //non abstract method
	 *           }
	 *           
	 *   b> Interface:
	 *      # same as class which contains public abstract methods and public static final variables.
	 *      # we can't create object of interface.
	 *      # every interface must have atleast one implementation class and impl class must override abstract methods.
	 *      # 100% abstraction. 
	 *      
	 *        syntax:
	 *        
	 *          interface interface_name{
	 *          
	 *              //public static final variables
	 *              
	 *              //public abstract methods
	 *              
	 *           }
	 *      
	 */

}
